            function Valider()
            {
                var prixprec = parseFloat(document.getElementById('prix').value);
                var DateCollecte = document.getElementById('dateCollecte').value;
                var DateLiv = document.getElementById('dateLiv').value;

                var dc = DateCollecte.replace(/-/g , '');
                var dl = DateLiv.replace(/-/g , '');

                if(prixprec == 0) 
                {
                    
                    alert("Vous n'avez rien choisi !");
                    
                    return false;
                }

                if(prixprec < 300) 
                {
                    
                    alert("Le montant minimum d'une commande est 300 DZD ");
                    
                    return false;
                }

                if(DateCollecte=="" ) 
                {
                    
                    alert("vous n'avez pas selectionné une date de collecte !");
                    
                    return false;
                }
                if(DateLiv == "") 
                {
                    
                    alert("vous n'avez pas selectionné une date de livraison !");
                    
                    return false;
                }
                if(dl < dc) 
                {
                    
                    alert("La date de livraison précède la date de collecte !");
                    
                    return false;
                }

            }
